package com.example.myapplication1.Models;

public class GDchild {
    private String mid;
    private String a3mal;
    private String finall;
    private String darajaa;
    private String tagdeer;

    public GDchild(String mid, String a3mal, String finall, String darajaa, String tagdeer) {
        this.mid = mid;
        this.a3mal = a3mal;
        this.finall = finall;
        this.darajaa = darajaa;
        this.tagdeer = tagdeer;
    }

        public String getMid() {
            return mid;
        }

        public void setMid(String mid) {
            this.mid = mid;
        }

        public String getA3mal() {
            return a3mal;
        }

        public void setA3mal(String a3mal) {
            this.a3mal = a3mal;
        }

        public String getFinall() {
            return finall;
        }

        public void setFinall(String finall) {
            this.finall = finall;
        }

        public String getDarajaa() {
            return darajaa;
        }

        public void setDarajaa(String darajaa) {
            this.darajaa = darajaa;
        }

        public String getTagdeer() {
            return tagdeer;
        }

        public void setTagdeer(String tagdeer) {
            this.tagdeer = tagdeer;
        }
    }

